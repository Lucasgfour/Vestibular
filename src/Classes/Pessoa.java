package Classes;

public class Pessoa {
	private String nome;
	private String endereco;
	private String cidade;
	private String estado;
	private String cpf;
	private String rg;
	private String telresidencia;
	private String telcelular;
	private String email;
	private String titeleitor;
	private String pai;
	private String mae;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getCidade() {
		return cidade;
	}
	public void setCidade(String cidade) {
		this.cidade = cidade;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getRg() {
		return rg;
	}
	public void setRg(String rg) {
		this.rg = rg;
	}
	public String getTelresidencia() {
		return telresidencia;
	}
	public void setTelresidencia(String telresidencia) {
		this.telresidencia = telresidencia;
	}
	public String getTelcelular() {
		return telcelular;
	}
	public void setTelcelular(String telcelular) {
		this.telcelular = telcelular;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTiteleitor() {
		return titeleitor;
	}
	public void setTiteleitor(String titeleitor) {
		this.titeleitor = titeleitor;
	}
	public String getPai() {
		return pai;
	}
	public void setPai(String pai) {
		this.pai = pai;
	}
	public String getMae() {
		return mae;
	}
	public void setMae(String mae) {
		this.mae = mae;
	}
	
	
}
